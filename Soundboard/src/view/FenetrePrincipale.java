    private void pauseButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pauseButtonMouseClicked
        // TODO add your handling code here:
        System.out.println("Pause");

        if (!isPaused) {
            try {
                mp.pause();
                isPaused = true;
                imageIcon = new ImageIcon(getClass().getResource("/view/icon/ic_play_circle_filled_white_24dp_2x.png"));
                pauseButton.setIcon(imageIcon);
            } catch (IOException ex) {
                Logger.getLogger(FenetrePrincipale.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                mp.resume();
                isPaused = false;
                imageIcon = new ImageIcon(getClass().getResource("/view/icon/ic_pause_circle_filled_white_24dp_2x.png"));
                pauseButton.setIcon(imageIcon);
            } catch (JavaLayerException ex) {
                Logger.getLogger(FenetrePrincipale.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(FenetrePrincipale.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_pauseButtonMouseClicked

