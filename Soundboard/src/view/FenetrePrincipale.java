    private void chooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseFileActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("MP3", "mp3");
        fc.setFileFilter(fileFilter);
        fc.setAcceptAllFileFilterUsed(false);

        Action details = fc.getActionMap().get("viewTypeDetails");
        details.actionPerformed(null);
        fc.setCurrentDirectory(new File("S:\\Musique"));

        int buttonPressed = fc.showOpenDialog(FenetrePrincipale.super.getParent());

        if (buttonPressed == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            son = new Son(file);
            ListeDesSons.addSon(son);

            JMenuItem menuItem = (JMenuItem) evt.getSource();
            JPopupMenu popMenu = (JPopupMenu) menuItem.getParent();
            System.out.println(son.getName());

            for (String s : buttonMap.keySet()) {
                if (buttonMap.get(s).getComponentPopupMenu().equals(popMenu)) {
                    buttonMap.get(s).setText(son.getName());
                    break;
                }
            }

        }
        this.revalidate();
        this.pack();
        this.repaint();
    }//GEN-LAST:event_chooseFileActionPerformed

